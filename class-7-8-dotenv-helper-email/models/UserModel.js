const mongoose = require("mongoose");
const {Schema} = mongoose;

let UserSchema = new Schema({
    name: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
    isVerified: {
        type: Boolean,
        default: false,
    }
});

module.exports = mongoose.model("User", UserSchema);